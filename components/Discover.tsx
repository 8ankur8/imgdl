
import React from 'react';
import { NextPage } from 'next';
import Link from 'next/link';
import { useRouter } from 'next/router';

import { topics } from '../utils/constants';

const Discover: NextPage = () => {
  const router = useRouter();
  const { topic } = router.query;

  const activeTopicStyle = 'text-gray-200 border-2 hover:text-[#F93166] border-[#31F9DB] px-6 py-1 rounded-full flex items-center gap-2 justify-center cursor-pointer text-[#F93166]';
  const topicStyle = 'text-gray-200 border-2 hover:text-[#F93166] border-gray-300 px-4 py-1 rounded-full flex items-center gap-2 justify-center cursor-pointer text-black';

  return (
    <div className='pb-6'>
      <div className='flex gap-x-4 gap-y-2 flex-wrap'>
        {topics?.map((item) => (
          <Link href={`/?topic=${item.name}`} key={item.name}>
            <div className={topic === item.name ? activeTopicStyle : topicStyle}>
              <span className='font-bold text-2xl text-lg xl:text-md '>
                {item.icon}
              </span>
              <span className={`font-medium text-lg  sm:block capitalize hidden`}>
                {item.name}
              </span>
            </div>
          </Link>
        ))}
      </div>
    </div>
  )} 
  
  export default Discover;
    
    
    
    
    
    


































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































    
    
    
    





















    


